    public void anadirFilaTabla() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[3];

        if (!jTable1.getSelectionModel().isSelectionEmpty()) {
            for (int i = 0; i < vertices.size(); i++) {
                rowData[0] = vertices.get(i).getX();
                rowData[1] = vertices.get(i).getY();
                model.addRow(rowData);
            }
        } else {
            model.setRowCount(0);

            for (int i = 0; i < vertices.size(); i++) {
                rowData[0] = vertices.get(i).getID();
                rowData[1] = vertices.get(i).getX();
                rowData[2] = vertices.get(i).getY();
                model.addRow(rowData);
            }
        }
    }

    public void anadirFilaAristas(ArrayList<Arista> aristas)
    {
        DefaultTableModel model = (DefaultTableModel) aristasTable.getModel();
        Object rowData[] = new Object[3];
        model.setRowCount(0);
        
        
            aristas.forEach((Arista a)-> {
                rowData[0] = a.getOrigen().getID();
                rowData[1] = a.getDestino().getID();
                rowData[2] = a.getCoste();
                
                model.addRow(rowData);
            });
    }
    
