    private void aleatoriosButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aleatoriosButActionPerformed
        vertices.clear();
        try {
            double max = Double.parseDouble(rangoDchPane.getText());
            double min = Double.parseDouble(rangoIzqPane.getText());

            if (aleatoriosPane.getText().matches("[0-9]+")) {
                for (int i = 0; i < Integer.parseInt(aleatoriosPane.getText()); i++) {
                    double x = (Math.random() * (max - min) + min);
                    double y = (Math.random() * (max - min) + min);
                    vertices.add(new Vertice(i + 1, x, y));
                }
                this.actualizarDatos();

            } else {
                JOptionPane.showMessageDialog(this, "Número inválido!", "Número Inválido!", JOptionPane.ERROR_MESSAGE);
            }

        } catch (java.lang.NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Rango inválido!", "Rango Inválido!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_aleatoriosButActionPerformed

