    private void generarGrafo()
    {
        this.g = new Grafo();
        
        vertices.forEach((v) -> {    //Añadimos cada vertice al grafo
            g.anadirVertice(v);
        });
        
        for(int i=0; i<this.vertices.size(); i++)
            for(int j=i+1; j<this.vertices.size(); j++)
            {
                Arista a = new Arista(vertices.get(i), vertices.get(j));
                g.anadirArista(a);       
            }
    }
